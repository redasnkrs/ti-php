      Paris se situe au cœur d'un vaste bassin sédimentaire aux sols fertiles et au climat tempéré, le bassin parisien, sur une boucle de la Seine, entre les confluents de celle-ci avec la Marne et l'Oise.

      Au milieu du Bassin parisien, deux îles sur la Seine constituent le cœur historique de Paris : l'île Saint-Louis, la plus à l'est et l'île de la Cité, la plus à l'ouest. La ville s'étend de part et d'autre du fleuve, sur une superficie environ deux fois supérieure au nord, sur la rive droite, à celle au sud, sur la rive gauche.

      Paris intra-muros, délimité par l'enceinte de Thiers en 1844 et administrativement en 1860, est séparé de ses communes limitrophes par le boulevard périphérique, une voie rapide urbaine de 35 km. Les accès routiers se font par les portes de Paris et les routes et autoroutes rejoignant cette rocade. Deux grands espaces boisés, le bois de Boulogne et le bois de Vincennes, ont été aménagés par le baron Haussmann et rattachés à Paris en 1929, portant le périmètre de la ville à 54,74 km².

      La superficie de Paris est de 105,40 km², avec une surface de 84,45 km² en excluant les bois de Boulogne et de Vincennes. L'unité urbaine de Paris s'étend sur 2 845 km² et rassemble 10 659 489 habitants en 2014. Le point zéro des routes de France est situé devant Notre-Dame de Paris.

      La Seine traverse la ville en formant un arc de cercle, avec plus de trente ponts permettant de la franchir. La ville est également traversée par la Bièvre, aujourd'hui souterraine, et par le canal Saint-Martin, qui alimente le bassin de la Villette et rejoint la Seine en amont de l'île Saint-Louis.

      Paris a un climat océanique dégradé, avec des températures minimales moyennes de 15,1 °C en été et de 3 °C en hiver. Les pluies sont fréquentes toute l'année, mais moins abondantes que sur les côtes. Le développement de l'urbanisation a provoqué une augmentation de la température et une baisse du nombre de jours de brouillard. Au cours du XXe siècle, le climat de Paris est devenu plus doux et légèrement plus arrosé.

      En raison du réchauffement climatique, le climat de Paris à la fin du XXIe siècle sera plus chaud, avec un nombre de journées estivales par an augmentant de 10 à 60 jours. Les canicules deviendront plus fréquentes, intenses et longues, et les hivers plus doux et arrosés. Le nombre de jours très chauds pourrait atteindre 10 à 45 jours par an à la fin du siècle. Des vagues de chaleur exceptionnelles sont attendues, et un été comme celui de 2003 deviendrait fréquent.
